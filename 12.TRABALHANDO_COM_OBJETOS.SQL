--TRABALHANDO COM SEQUENCE. UMA SEQUENCIA NÃO ALTERA SEU VALOR ATUAL. É NECESSARIO DROP.


CREATE SEQUENCE SEQ_ALUNO1

--VALOR INICIAL
START WITH 60

--QUANTIDADE A INCREMENTAR
INCREMENT BY 2

--VALOR MINIMO
MINVALUE 60

--VALOR MAXIMO
MAXVALUE 100

--NÃO GUARDAR EM CACHE FAIXA DE VALORES COM PADRAO, NO CASO -> 20
NOCACHE

--MANTÉM O PADRÃO ENTRE VALOR MIN E VALOR MAX -> 60 E 100
NOCYCLE;



INSERT INTO TALUNO (COD_ALUNO, NOME)
VALUES (SEQ_ALUNO1.NEXTVAL, 'MASTER TRANING2');

SELECT * FROM TALUNO;



--BUSCANDO SEQUENCIAS CRIADAS PARA O USUÁRIO
SELECT * FROM USER_SEQUENCES;

--BUSCANDO VALOR ATUAL DA SEQUENCIA
SELECT SEQ_ALUNO1.CURRVAL AS VALOR_ATUAL FROM DUAL;


--COMANDO PARA ALTERAR ALGUM VALOR DA SEQUENCIA (NO CASO, VALOR MÁXIMO)
