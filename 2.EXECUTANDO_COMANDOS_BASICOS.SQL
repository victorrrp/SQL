SELECT COD_ALUNO, NOME, CIDADE
FROM TALUNO;

SELECT COD_ALUNO AS "CÓDIGO", NOME AS "NOME DO ALUNO"
FROM TALUNO;


-- DISTINCT RETIRA OS REGISTROS DUPLOS (LINHAS DUPLICADAS)
SELECT CIDADE FROM TALUNO;

SELECT DISTINCT CIDADE FROM TALUNO;


-- ORDER BY --> ORDENADO POR (NESSE CASO O DISTINCT NÃO IRÁ RETIRAR, POIS NÃO TEM LINHA DUPLICADA)
SELECT DISTINCT CIDADE, COD_ALUNO
FROM TALUNO
ORDER BY CIDADE;


-- FAZENDO CÁLCULOS A PARTIR DA TABELA
-- UTILIZANDO 'AS' PARA RENOMEAR A COLUNA
-- 'APELIDO' DE COLUNA SÓ FUNCIONA EM ORDER BY
SELECT NOME FROM TCURSO;

SELECT NOME AS NOME_DO_CURSO, 
       VALOR,
       CARGA_HORARIA,
       ROUND(VALOR/CARGA_HORARIA, 2) AS VALOR_HORA --UTILIZANDO ROUND PARA ARRENDONDAR
FROM TCURSO
ORDER BY VALOR_HORA;


-- ALTERANDO O DESCONTO DA TABELA TCONTRATO PARA VALOR NULO
UPDATE TCONTRATO SET
DESCONTO = NULL
WHERE COD_CONTRATO = 4;

SELECT * FROM TCONTRATO;


-- NÃO É POSSÍVEL FAZER CALCULOS COM COLUNA NÚMERICA DE VALOR NULO. EX.:
SELECT COD_CONTRATO,
       TOTAL,
       DESCONTO,
       TOTAL+DESCONTO
FROM TCONTRATO;


-- NVL - FUNÇÃO DO ORACLE QUE TRATA DE COLUNAS NULAS (QUALQUER TIPO, NÃO SÓ NUMERICAS)
SELECT COD_CONTRATO,
       DESCONTO,
       NVL(DESCONTO,0), -- SE A COLUNA EM QUESTÃO FOR NULA, ELE RETORNARÁ 0
       TOTAL,
       TOTAL+NVL(DESCONTO,0) AS TOTAL_MAIS_DESCONTO
FROM TCONTRATO;


-- CONCATENAÇÃO DE COLUNAS
SELECT COD_ALUNO || '-' || NOME || '//' || CIDADE AS ALUNO_CIDADE
FROM TALUNO
ORDER BY COD_ALUNO;


