--TRABALHANDO COM IF, THEN, ELSE

DECLARE
  VNOME VARCHAR(30)   := 'CARIOCA';
  VCIDADE VARCHAR(30);
  VESTADO VARCHAR(2);
BEGIN
  IF(VNOME = 'GAUCHO') THEN
    VCIDADE := 'PORTO ALEGRE';
    VESTADO := 'RS';
  ELSIF(VNOME = 'CARIOCA')THEN
    VCIDADE := 'RIO DE JANEIRO';
    VESTADO := 'RJ';
  ELSE
    IF(VNOME='PAULISTA') THEN
      VCIDADE := 'SAO PAULO';
      VESTADO :='SP';
    ELSE
      VCIDADE := 'OUTROS';
      VESTADO := 'XX';
    END IF;
  END IF;
  DBMS_OUTPUT.PUT_LINE(VCIDADE||'-'||VESTADO);
END;

--EXEMPLO DE CASE
DECLARE
  VESTADO VARCHAR2(2) := 'RR';
  VNOME VARCHAR2(30);
BEGIN
  CASE
    WHEN VESTADO ='RS' THEN VNOME := 'GAUCHO';
    WHEN VESTADO ='RJ' OR VESTADO = 'ES' THEN VNOME := 'CARIOCA';
  ELSE
    VNOME := 'OUTROS';
  END CASE;                                             --SEMPRE TERMINAR O PARÂMETRO
  DBMS_OUTPUT.PUT_LINE('APELIDO: '||VNOME);
END;


--LAÇO DE REPETIÇÃO (LOOP)
DECLARE
  VCONTADOR INTEGER := 0;
BEGIN
  LOOP
    VCONTADOR := VCONTADOR + 1;
    DBMS_OUTPUT.PUT_LINE(VCONTADOR);
    EXIT WHEN VCONTADOR = 10;
  END LOOP;
  DBMS_OUTPUT.PUT_LINE('FIM DO LOOP');
END;


